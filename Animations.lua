local currentTrack = nil

function Animations.Play(id, UI)
	local hum = lp.Character and lp.Character:FindFirstChildOfClass("Humanoid")
	if not hum then return end
	if currentTrack then pcall(function() currentTrack:Stop(0) end) currentTrack = nil end
	local anim = Instance.new("Animation")
	anim.AnimationId = "rbxassetid://" .. tostring(id)
	local ok, track = pcall(function()
		local t = hum.Animator:LoadAnimation(anim)
		t.Priority = Enum.AnimationPriority.Action4
		t:Play()
		return t
	end)
	if ok and track then
		currentTrack = track
		if UI then UI.Notify("Playing animation", "Success") end
	else
		if UI then UI.Notify("Failed to load animation", "Error") end
	end
end

function Animations.Stop(UI)
	if currentTrack then
		pcall(function() currentTrack:Stop(0) end)
		currentTrack = nil
		if UI then UI.Notify("Animation stopped", "Warn") end
	end
end

function Animations.HandleChat(msg, UI)
	local cmd = msg:lower():split(" ")[1]
	if cmd == "stopanim" then Animations.Stop(UI) end
end

return Animations
